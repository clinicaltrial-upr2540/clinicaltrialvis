DROP VIEW IF EXISTS curated.commondescriptors CASCADE;

CREATE OR REPLACE VIEW curated.commondescriptors
(
  primary_key,
  drug_name,
  name,
  labeller,
  ndc_product_code,
  dpd_id,
  ema_product_code,
  ema_ma_number,
  started_marketing_on,
  ended_marketing_on,
  dosage_form,
  strength,
  route,
  fda_application_number,
  generic,
  over_the_counter,
  approved,
  country,
  source,
  type,
  description,
  c_number,
  unii,
  average_ms,
  monoisotopic_ms,
  state,
  synthesis_reference,
  indication,
  pharmacodynamics,
  mechanism_of_action,
  metabolism,
  absorption,
  half_life,
  protein_binding,
  route_of_elimination,
  volume_of_distribution,
  clearance,
  international_brands
)
AS 
 SELECT commondescriptor.primary_key,
    commondescriptor.drug_name,
    commondescriptor.name,
    commondescriptor.labeller,
    commondescriptor.ndc_product_code,
    commondescriptor.dpd_id,
    commondescriptor.ema_product_code,
    commondescriptor.ema_ma_number,
    commondescriptor.started_marketing_on,
    commondescriptor.ended_marketing_on,
    commondescriptor.dosage_form,
    commondescriptor.strength,
    commondescriptor.route,
    commondescriptor.fda_application_number,
    commondescriptor.generic,
    commondescriptor.over_the_counter,
    commondescriptor.approved,
    commondescriptor.country,
    commondescriptor.source,
    commondescriptor.type,
    commondescriptor.description,
    commondescriptor.c_number,
    commondescriptor.unii,
    commondescriptor.average_ms,
    commondescriptor.monoisotopic_ms,
    commondescriptor.state,
    commondescriptor.synthesis_reference,
    commondescriptor.indication,
    commondescriptor.pharmacodynamics,
    commondescriptor.mechanism_of_action,
    commondescriptor.metabolism,
    commondescriptor.absorption,
    commondescriptor.half_life,
    commondescriptor.protein_binding,
    commondescriptor.route_of_elimination,
    commondescriptor.volume_of_distribution,
    commondescriptor.clearance,
    commondescriptor.international_brands
   FROM curated.commondescriptor;

